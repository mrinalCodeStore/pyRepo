#bitwise operator is use to perform operation on the binary values of an variable



#-------bitwise OR operation--------------

a=20  # 00010100
b=30  # 00011110

c=a|b # 00011110 = 30

print(c)

#-------bitwise AND operation--------------

a=20  # 00010100
b=30  # 00011110

c=a&b # 00010100 = 20

print(c)


#------bitwise SHIFT operation

e=10<<2 #00001010  <<2 i.e left shift by 2 bit position so,   00101000=40

print(e)

e=10>>2 #00001010  >>2 i.e right shift by 2 bit position so,   00000010=2

print(e)


#-------bitwise EX OR operation--------------

a=20  # 00010100
b=30  # 00011110

c=a^b # 00001010 = 10

print(c)



